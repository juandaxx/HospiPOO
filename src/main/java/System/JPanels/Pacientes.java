    //Cargamos todos los pacientes del sistema y los ponemos en una tabla
    public void cargarPacientes() {
        DefaultTableModel model = (DefaultTableModel) TblPacientes.getModel();
        model.setRowCount(0); // limpiar tabla

        TPacientes tpac = new TPacientes();
        List<Object[]> lista = tpac.obtenerPacientes();

        for (Object[] fila : lista) {
            model.addRow(fila);
        }
    }
    
    //Diseño de la tabla de pacientes
    public void diseño(){
        // Diseño del encabezado
        TblPacientes.setAutoCreateRowSorter(true);
        TblPacientes.getTableHeader().setOpaque(false);
        TblPacientes.getTableHeader().setBackground(new java.awt.Color(30, 30, 30));
        TblPacientes.getTableHeader().setForeground(java.awt.Color.WHITE);
        TblPacientes.getTableHeader().setFont(new java.awt.Font("Segoe UI", java.awt.Font.BOLD, 14));

        // Altura de filas
        TblPacientes.setRowHeight(30);

        // Bordes y color de líneas
        TblPacientes.setShowHorizontalLines(true);
        TblPacientes.setShowVerticalLines(false);
        TblPacientes.setGridColor(new java.awt.Color(220, 220, 220));

        // Fuente del contenido
        TblPacientes.setFont(new java.awt.Font("Segoe UI", java.awt.Font.PLAIN, 13));

        // Renderer para filas, zebra rows, selección y centrado del texto
        javax.swing.table.DefaultTableCellRenderer renderer = new javax.swing.table.DefaultTableCellRenderer() {
            @Override
            public java.awt.Component getTableCellRendererComponent(
                    javax.swing.JTable table, Object value, boolean isSelected,
                    boolean hasFocus, int row, int column) {

                java.awt.Component c = super.getTableCellRendererComponent(
                        table, value, isSelected, hasFocus, row, column);

                // CENTRAR EL TEXTO
                setHorizontalAlignment(javax.swing.SwingConstants.CENTER);

                // Color de texto normal
                c.setForeground(java.awt.Color.BLACK);

                if (isSelected) {
                    c.setBackground(new java.awt.Color(30, 144, 255)); // azul suave
                    c.setForeground(java.awt.Color.WHITE);
                } else {
                    // Zebra rows
                    c.setBackground(row % 2 == 0
                            ? new java.awt.Color(245, 245, 245)
                            : java.awt.Color.WHITE);
                }

                return c;
            }
        };

        // Aplicamos el renderer como predeterminado para toda la tabla
        TblPacientes.setDefaultRenderer(Object.class, renderer);
    }

    
