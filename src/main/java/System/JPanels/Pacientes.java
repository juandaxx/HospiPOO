    //Filtro de la tabla de pacientes
    private void filtrar() {
        String placeholder = "Buscar paciente...";
        String texto = TxtBuscar.getText();
        if (texto == null) texto = "";

        DefaultTableModel model = (DefaultTableModel) TblPacientes.getModel();

        // Obtenemos el sorter actual (si existe)
        TableRowSorter<DefaultTableModel> sorter = null;
        if (TblPacientes.getRowSorter() instanceof TableRowSorter) {
            sorter = (TableRowSorter<DefaultTableModel>) TblPacientes.getRowSorter();
        }

        // Si está vacío o es el placeholder -> quitamos cualquier filtro y devolvemos todos los registros
        if (texto.trim().isEmpty() || texto.equals(placeholder)) {
            if (sorter != null) {
                sorter.setRowFilter(null);         // deja visible todo
            } else {
                TblPacientes.setRowSorter(null);   // no hay sorter: asegurar que no haya filtro
            }
            return;
        }

        // Si no hay sorter, creamos uno nuevo
        if (sorter == null) {
            sorter = new TableRowSorter<>(model);
            TblPacientes.setRowSorter(sorter);
        }

        // Aplicamos filtro (case-insensitive). Usamos Pattern.quote para escapar el texto del usuario.
        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + java.util.regex.Pattern.quote(texto)));
    }

    
    //Le agregamos un placeholder al campo de filtro
    private void agregarPlaceholder(javax.swing.JTextField campo, String texto) {
        campo.setText(texto);
        campo.setForeground(new java.awt.Color(150, 150, 150));

        campo.addFocusListener(new java.awt.event.FocusAdapter() {
            @Override
            public void focusGained(java.awt.event.FocusEvent e) {
                if (campo.getText().equals(texto)) {
                    campo.setText("");
                    campo.setForeground(new java.awt.Color(0, 0, 0));
                }
            }

            @Override
            public void focusLost(java.awt.event.FocusEvent e) {
                if (campo.getText().isEmpty()) {
                    campo.setText(texto);
                    campo.setForeground(new java.awt.Color(150, 150, 150));
                }
            }
        });
    }

