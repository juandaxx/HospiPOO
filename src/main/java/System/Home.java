    //Creamos un reloj que muestre fecha y hora actual
    private void iniciarReloj() {
        javax.swing.Timer timer = new javax.swing.Timer(1000, (e) -> {
            java.time.LocalDateTime ahora = java.time.LocalDateTime.now();
            String formato = ahora.format(java.time.format.DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss"));
            LblHora.setText(formato);
        });
        timer.start();
    }
    
    //Inicialisamos los paneles y los creamos
    private void cargarPaneles(){
        cardLayout = new CardLayout();
        PnlContenido.setLayout(cardLayout);
        PnlContenido.add(new Dashboard(), "dashboard");
        PnlContenido.add(new Usuarios(), "usuarios");
        PnlContenido.add(new Pacientes(), "pacientes");
        panelOrdenes = new Ordenes();
        panelConsultas = new Consultas(panelOrdenes);
        PnlContenido.add(panelOrdenes, "ordenes");
        PnlContenido.add(panelConsultas, "consultas");
        PnlContenido.add(new HistoriasClinicas(), "historias");
    }
